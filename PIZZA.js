function computeIngredients(pizzatype, difficulty, leaventype){
  var lato1 = document.querySelector("#input_lato1").value;
  var lato2 = document.querySelector("#input_lato2").value;
  var idratazione = null;
  if(difficulty!=null)
    {console.log(difficulty);
    idratazione = params[difficulty]["hydration"]/100;}
  else
    idratazione = document.querySelector("#input_idratazione").value/100;
  var coefficiente = null;
  if(pizzatype!=null)
    coefficiente = params[pizzatype]["coefficient"]/100;
  else
    coefficiente = document.querySelector("#input_coefficienteimpasto").value/100;
  console.log("coefficiente impasto: "+coefficiente);
  var percentsale = document.querySelector("#input_sale").value/100;
  var percenthoney = document.querySelector("#input_honey").value/100;
  var percenttomato = document.querySelector("#input_tomato").value/100;
  var percentmozzarella = document.querySelector("#input_mozzarella").value/100;
  var percentoil = document.querySelector("#input_oil").value/100;
  var num_teglie = document.querySelector("#input_numeroteglie").value;
  var impastototale = lato1*lato2*coefficiente*num_teglie;
  var farina = impastototale/(1+idratazione);
  var lievitodibirra = 0;
  var lievitomadre = 0;

  if(leaventype==null){
    leaventype = document.querySelector("#input_leaventype").value;
  }
  if(leaventype==="yeast"){
    var lievitodibirra = farina*params["yeast"]["percent"]/100;
    var lievitomadre = 0;
  }
  if(leaventype==="sourdough"){
    var lievitomadre = farina*params["sourdough"]["percent"]/100;
    impastototale = impastototale-lievitomadre;
    var lievitodibirra = 0;
  }

  farina = impastototale/(1+idratazione);
  var acqua = impastototale-farina;
  var sale = farina*percentsale;
  var honey = farina*percenthoney;
  var tomato = farina*percenttomato;
  var mozzarella = farina*percentmozzarella;
  var oil = farina*percentoil;
  impastototale = impastototale + lievitomadre;
  
    
  var ingredients = {
    impastototale: impastototale.toFixed(2),
    farina: farina.toFixed(2),
    acqua: acqua.toFixed(2),
    sale: sale.toFixed(2),
    honey: honey.toFixed(2),
    tomato: tomato.toFixed(2),
    mozzarella: mozzarella.toFixed(2),
    oil: oil.toFixed(2),
    lievitodibirra: lievitodibirra.toFixed(2),
    lievitomadre: lievitomadre.toFixed(2)
  };
  return ingredients;
}

